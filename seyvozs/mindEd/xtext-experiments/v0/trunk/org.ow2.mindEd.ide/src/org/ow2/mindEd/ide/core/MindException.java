package org.ow2.mindEd.ide.core;

public class MindException extends Exception {

	public MindException() {
		super();
	}

	public MindException(String message, Throwable cause) {
		super(message, cause);
	}

	public MindException(String message) {
		super(message);
	}

	public MindException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

}
